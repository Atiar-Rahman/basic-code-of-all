#include<bits/stdc++.h>
using namespace std;

int main(){
    string s="Hello,hello World!";
    cout<<"Size of the string: "<<s.size()<<endl;
    cout<<"Length of the string: "<<s.length()<<endl;
    cout<<"Capacity of the string: "<<s.capacity()<<endl;
    cout<<"Max size of the string: "<<s.max_size()<<endl;
    return 0;
}